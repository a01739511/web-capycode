def build_order_items(lines):
    ids = [chr(ord("A") + index) for index in range(len(lines))]
    return [{"id": ids[index], "text": line} for index, line in enumerate(lines)], ids


def build_select_items(lines, correct_ids):
    ids = [chr(ord("A") + index) for index in range(len(lines))]
    items = [{"id": ids[index], "text": line} for index, line in enumerate(lines)]
    return items, correct_ids


def build_payload():
    payload = {"temas": {}}
    for route_key, levels in ROUTES.items():
        payload["temas"][route_key] = {}
        for level in levels:
            order_lines, order_ids = build_order_items(level["order_lines"])
            select_lines, select_ids = build_select_items(level["select_lines"], level["select_correct"])
            questions = [
                {
                    "tipo": "opcion_multiple",
                    "prompt": level["mc_prompt"],
                    **({"code": level["mc_code"]} if "mc_code" in level else {}),
                    "opciones": [{"id": option_id, "text": text} for option_id, text in level["mc_options"]],
                    "correct_ids": level["mc_correct"],
                },
                {
                    "tipo": "ordenar_lineas",
                    "prompt": level["order_prompt"],
                    "lineas": order_lines,
                    "orden_correcto": order_ids,
                },
                {
                    "tipo": "drag_and_drop",
                    "prompt": level["blank_prompt"],
                    "plantilla": level["template"],
                    "banco_palabras": level["word_bank"],
                    "rellenos": level["blanks"],
                },
                {
                    "tipo": "seleccionar_lineas",
                    "prompt": level["select_prompt"],
                    "lineas": select_lines,
                    "correct_ids": select_ids,
                },
                {
                    "tipo": "respuesta_numerica",
                    "prompt": level["num_prompt"],
                    "code": level["num_code"],
                    "valor": level["num_value"],
                },
            ]
            payload["temas"][route_key][f'nivel_{level["nivel"]}'] = questions
    return payload


def main():
    payload = build_payload()
    json_text = json.dumps(payload, ensure_ascii=False, indent=2)
    (ROOT / "questions.json").write_text(json_text, encoding="utf-8")
    (ROOT / "js" / "questions-data.js").write_text("window.CAPYCODE_QUESTIONS = " + json_text + ";\n", encoding="utf-8")
    total_levels = sum(len(levels) for levels in payload["temas"].values())
    print("Temas generados:", ", ".join(payload["temas"].keys()))
    print("Niveles generados:", total_levels)


if __name__ == "__main__":
    main()
